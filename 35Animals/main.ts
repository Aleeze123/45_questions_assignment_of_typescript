// Store animal names in a list
let animals: string[] = ["Cats", "Deer", "Rabbit"];

// use a for loop to print out the name of each animal. 
for (let i = 0 ; i < animals.length; i++){
    console.log(`A ${animals[i]} would make a great pet.`);
}

